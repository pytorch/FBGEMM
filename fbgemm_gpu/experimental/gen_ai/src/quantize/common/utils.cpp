#include "fbgemm_gpu/quantize/utils.h" // @manual

namespace fbgemm_gpu {

int nextPowerOf2(int n) {
  if (n == 0) {
    return 1;
  }
  n--;
  n |= n >> 1;
  n |= n >> 2;
  n |= n >> 4;
  n |= n >> 8;
  n |= n >> 16;
  return n + 1;
}

} // namespace fbgemm_gpu
